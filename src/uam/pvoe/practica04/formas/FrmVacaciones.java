    public void llenarEstados(){
        Operaciones operaciones = new Operaciones(); 
        LinkedList<Estado> estados = operaciones.llenarListaEstados(); 
        for(Estado estado : estados){
            cmbEstados.addItem(estado);
        }
        
    }
    
    public void llenarAtractivos(String claveEdo){
        Operaciones operaciones = new Operaciones(); 
        LinkedList<Atractivo> atractivos = operaciones.llenarListaAtractivos(claveEdo); 
        for(Atractivo atractivo : atractivos){
            cmbAtractivos.addItem(atractivo);
        }
    }
    
