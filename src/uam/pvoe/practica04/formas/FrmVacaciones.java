    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        
        System.out.println("------------------------------- REGISTRO COMPLETADO ---------------------------");
        Estado estado = (Estado) cmbEstados.getSelectedItem();
        System.out.println("CLAVE ESTADO: " + estado.getClave());
        Atractivo atractivo = (Atractivo) cmbAtractivos.getSelectedItem(); 
        System.out.println("CLAVE ATRACTIVO: " + atractivo.getAtractivo());
        
        //verificamos el tipo de transporte 
        Enumeration<AbstractButton> btnGrp = btnGpViaje.getElements(); 
        while(btnGrp.hasMoreElements()){
            JRadioButton btnAux = (JRadioButton) btnGrp.nextElement(); 
            if(btnAux.isSelected()){
                MedioTransporte medio = (MedioTransporte) btnAux.getClientProperty("key"); 
                System.out.println("CLAVE DE MEDIO DE TRANSPORTE: " + medio.getClave());
            }
        }
        
        //verificamos los servicios 
        System.out.println("CLAVE DE SERVICIOS INCLUIDOS: ");
        int cont = 0; 
        for(JCheckBox aux : listaChk){
            if(aux.isSelected()){
                Servicio servicio = (Servicio) aux.getClientProperty("llave");
                System.out.println(servicio.getClave());
                cont++; 
            }
        }
        if(cont == 0){
            System.out.println("NO INCLUYO SERVICIOS");
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed
    
    public void mostrarAtractivos(){
        Estado estado = (Estado) cmbEstados.getSelectedItem(); 
        int indice = cmbEstados.getSelectedIndex(); 
        
        if(indice != 0){
            cmbAtractivos.removeAllItems();
            llenarAtractivos(estado.getClave());
            mostrarTodo(); 
            
        }else{
            ocultarTodo();
        }
    }
    
    
    
